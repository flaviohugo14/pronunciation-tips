</p>

<p>When that happens, do not attempt to instill artificial life into my body by the use of a machine. And don't call this my deathbed. Let it be called the bed of life, and let my body be taken from it to help others lead fuller lives. </p>


<ul>
<li>Give my sight to the man who has never seen a sunrise, a baby's face or love in the eyes of a woman.
</li>

<li>Give my heart to a person whose own heart has caused nothing but endless days of pain.
</li>

<li>Give my blood to the teenager who was pulled from the wreckage of his car, so that he might live to see his grandchildren play.
</li> 

<li>Give my kidneys to the one who depends on a machine to exist from week to week.
</li> 

<li>Take my bones, every muscle, every fiber and nerve in my body and find a way to make a crippled child walk. 
Explore every corner of my brain.
</li> 

<li>Take my cells, if necessary, and let them grow so that, someday a speechless boy will shout at the crack of a bat and a deaf girl will hear the sound of rain agianst her window.
</li> 

<li>Burn what is left of me and scatter the ashes to the winds to help the flowers grow.
</li> 

<li>If you must bury something, let it be my faults, my weakness and all prejudice against my fellow man.
</li> 

<li>Give my sins to the devil.
</li> 

<li>Give my soul to God.
</li>
</ul>

